public class W1Datatypes2 {

    public class Main{
        public static void main(String args[])
        {
            int a=2;
            double b=3.45; 
            char c='S'; //Single quote-single letter
            String d="uraksha is the best"; //Double quote-multiple letters
            
            // If we want to join english sentence with variable we use + operator
            System.out.println("The value of a is " +a);
            System.out.println("Double can hold multiple decimal points " +b+ " for example");
            System.out.print(c);
            System.out.println(d);
        }
    }
}
