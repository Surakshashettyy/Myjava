public class W1Operator3 
{
    public static void main(String args[])
    {
        //Arithmetic operators and Assignment operator assigns values (=)
        int a=3; //box1 a = 3
        int b=2; //box2 b = 2
        int c=a+b; //box3 c = 3+2=5 (Addition operator)
        int d=a/b; //box4 d = 3/2 = 1.5 = 1 (Division operator)
        
        System.out.println(c);
        System.out.println(a-b); //(Subtraction operator)
        System.out.println(a*b); //(Multiplication operator)
        System.out.println(d);
        System.out.println(a%b); //(Modulus operator, remainder)
    }
}
